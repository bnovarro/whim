export function getMockVenues(params: WhimParams): Venue[] {
  let pool: Venue[];

  switch (params.activityType) {
    case 'dinner':
      pool = DINNER_VENUES;
      break;
    case 'drinks':
      pool = DRINKS_VENUES;
      break;
    case 'coffee':
      pool = COFFEE_VENUES;
      break;
    case 'activity':
      pool = ACTIVITY_VENUES;
      break;
    case 'watch_sports':
      pool = WATCH_SPORTS_VENUES;
      break;
    case 'sports':
      // Live events — no mock venue pool (handled by sportsService)
      return [];
    case 'whatever':
      pool = [
        ...DINNER_VENUES.slice(0, 4),
        ...DRINKS_VENUES.slice(0, 4),
        ...ACTIVITY_VENUES.slice(0, 3),
      ];
      break;
    default:
      pool = DINNER_VENUES;
  }

  // Soft-filter by cuisine preference if provided — keep full pool but boost matches via initial sort
  if (params.cuisine && params.activityType === 'dinner') {
    const cuisine = params.cuisine;
    pool = [
      ...pool.filter(v => v.tags.some(t => t === cuisine || t.includes(cuisine))),
      ...pool.filter(v => !v.tags.some(t => t === cuisine || t.includes(cuisine))),
    ];
  }

  // Soft-filter by bar type if provided
  if (params.barType && params.activityType === 'drinks') {
    const barType = params.barType.replace('_bar', '').replace('_', '_');
    pool = [
      ...pool.filter(v => v.tags.some(t => t.includes(barType) || t === params.barType)),
      ...pool.filter(v => !v.tags.some(t => t.includes(barType) || t === params.barType)),
    ];
  }

  return pool.sort((a, b) => b.matchScore - a.matchScore);
}
