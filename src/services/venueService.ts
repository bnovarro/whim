import axios from 'axios';
import { Venue, WhimParams, ActivityType } from '../types';

const GOOGLE_API_KEY = process.env.EXPO_PUBLIC_GOOGLE_PLACES_API_KEY || '';

const ACTIVITY_QUERY_MAP: Record<ActivityType, string> = {
  drinks: 'bar cocktail lounge',
  dinner: 'restaurant dinner',
  coffee: 'cafe coffee shop',
  activity: 'activity entertainment venue',
  whatever: 'popular venue',
  sports: 'sports stadium arena',
  watch_sports: 'sports bar',
};

export async function searchVenuesByParams(
  params: WhimParams,
  lat: number,
  lon: number
): Promise<Venue[]> {
  if (!GOOGLE_API_KEY) {
    return getMockVenues(params);
  }
  try {
    const radiusMeters = params.radiusMiles * 1609;
    const query = ACTIVITY_QUERY_MAP[params.activityType];
    const url = `https://maps.googleapis.com/maps/api/place/nearbysearch/json?location=${lat},${lon}&radius=${radiusMeters}&keyword=${encodeURIComponent(query)}&key=${GOOGLE_API_KEY}`;
    const response = await axios.get(url);
    return response.data.results.slice(0, 15).map((p: GooglePlace) => mapGooglePlaceToVenue(p, lat, lon, params.activityType));
  } catch {
    return getMockVenues(params);
  }
}

interface GooglePlace {
  place_id: string;
  name: string;
  vicinity: string;
  geometry: { location: { lat: number; lng: number } };
  rating?: number;
  price_level?: number;
  opening_hours?: { open_now: boolean };
  types: string[];
  photos?: { photo_reference: string }[];
}

function mapGooglePlaceToVenue(p: GooglePlace, userLat: number, userLon: number, activityType: ActivityType): Venue {
  const dist = haversineDistance(userLat, userLon, p.geometry.location.lat, p.geometry.location.lng);
  return {
    id: p.place_id,
    name: p.name,
    address: p.vicinity,
    neighborhood: inferNeighborhood(p.vicinity),
    distanceMiles: parseFloat(dist.toFixed(1)),
    rating: p.rating || 4.0,
    priceLevel: (p.price_level as 1 | 2 | 3 | 4) || 2,
    photos: p.photos?.slice(0, 3).map(ph => `https://maps.googleapis.com/maps/api/place/photo?maxwidth=400&photoreference=${ph.photo_reference}&key=${GOOGLE_API_KEY}`) || [],
    tags: p.types.slice(0, 4),
    isOpenNow: p.opening_hours?.open_now ?? true,
    activityType,
    matchScore: 0,
    matchExplanation: '',
    isAlternative: false,
  };
}

function haversineDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 3958.8;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLon = ((lon2 - lon1) * Math.PI) / 180;
  const a = Math.sin(dLat / 2) ** 2 + Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) * Math.sin(dLon / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

function inferNeighborhood(vicinity: string): string {
  const neighborhoods = [
    'Lower East Side', 'Williamsburg', 'West Village', 'East Village', 'SoHo',
    'Tribeca', "Hell's Kitchen", 'Chelsea', 'Midtown', 'Upper East Side',
    'Upper West Side', 'Nolita', 'DUMBO', 'Cobble Hill', 'Carroll Gardens',
    'Flatiron', 'Crown Heights', 'Bushwick', 'Park Slope', 'Financial District',
  ];
  for (const n of neighborhoods) {
    if (vicinity.includes(n)) return n;
  }
  return vicinity.split(',')[1]?.trim() || 'Manhattan';
}

